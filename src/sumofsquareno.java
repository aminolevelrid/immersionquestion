public class sumofsquareno {
    public static void main(String[] args) {
        int a=4;
        for(int i=0;i<a;i++){
            if(Math.sqrt(a)==2){
                int m=a-i;
                int b=a-i;
                if ((m * m + b * b)==a) {
                    System.out.print("true");

                }
            }
        }
    }
}
